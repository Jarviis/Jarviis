Jarviis.module("Users", function (Users, Jarviis, Backbone, Marionette, $, _) {
  Users.Router = Marionette.AppRouter.extend({
    appRoutes: {
      "": "indexUsers"
    }
  });

  var Controller = {
    indexUsers: function () {
      Jarviis.Users.Index.Controller.indexUsers();
    }
  };

  Jarviis.addInitializer(function () {
    new Users.Router({
      controller: Controller
   });
  });
});
